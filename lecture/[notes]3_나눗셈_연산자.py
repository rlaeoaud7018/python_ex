print(f'2 ** 30 = {2 ** 30} (약 1GB)')
print(f'2 ** 40 = {2 ** 40} (약 1TB)')

"""
위 코드 결과:
2 ** 10 = 1024 (약 1KB)
2 ** 20 = 1048576 (약 1MB)
2 ** 30 = 1073741824 (약 1GB)
2 ** 40 = 1099511627776 (약 1TB)

컴퓨터 메모리는 2의 거듭제곱으로 증가한다.


=================================================
핵심 함수 및 연산자 총정리
=================================================

/               → 나눗셈 (결과는 실수)
//              → 정수 나눗셈 (몫, 결과는 정수)
%               → 나머지
**              → 거듭제곱 (지수)

나눗셈과 관련된 식:
피제수 = (제수 × 몫) + 나머지
dividend = (divisor × quotient) + remainder


=================================================
핵심 정리
=================================================

네 연산자의 특징:

1. / (나눗셈)
   - 항상 실수(float) 결과
   - 10 / 2 = 5.0

2. // (정수 나눗셈)
   - 정수(int) 결과
   - 소수점 이하 버림
   - 10 // 3 = 3

3. % (나머지)
   - 나머지 값 반환
   - 10 % 3 = 1
   - 짝수/홀수 판별에 유용

4. ** (거듭제곱)
   - 밑을 지수만큼 곱함
   - 2 ** 3 = 8
   - 지수 0.5로 제곱근 계산 가능

실제 개발에서는:
- 평균 계산 (/)
- 몫 계산 (//)
- 짝수/홀수 판별 (%)
- 배수 판별 (%)
- 지수 계산 (**)
- 감염 확산, 성장률 모델링 (**)

등에서 사용된다.
"""
