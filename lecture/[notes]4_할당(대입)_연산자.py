"""
할당(대입) 연산자 완전 정리
=================================================

1. 할당 연산자란?
-------------------------------------------------
할당 연산자(또는 대입 연산자)는 변수에 값을 저장하는 연산자이다.

형태:
변수 = 값

비유:
할당 연산자는 상자에 물건을 넣는 행위와 같다.
변수(상자)에 값(물건)을 넣는다.
"""

num = 5

print(f'num = {num}')
print(f'type(num) = {type(num)}')

name = 'Alice'

print(f'name = {name}')
print(f'type(name) = {type(name)}')

score = 95.5

print(f'score = {score}')
print(f'type(score) = {type(score)}')

"""
위 코드 결과:
num = 5
type(num) = <class 'int'>
name = Alice
type(name) = <class 'str'>
score = 95.5
type(score) = <class 'float'>

= 연산자로 다양한 자료형의 값을 변수에 저장할 수 있다.


2. 할당 연산자의 특징
-------------------------------------------------

① 우측에서 좌측으로 진행된다
   → num = 5는 5를 num에 저장한다

② 같은 변수에 여러 번 할당할 수 있다
   → 이전 값은 새로운 값으로 덮어씌워진다

③ 여러 변수에 한 번에 할당할 수 있다
   → a = b = c = 10
"""

num = 10

print(f'처음 num: {num}')

num = 20

print(f'변경 후 num: {num}')

num = 30

print(f'다시 변경 후 num: {num}')

"""
위 코드 결과:
처음 num: 10
변경 후 num: 20
다시 변경 후 num: 30

같은 변수에 여러 번 할당하면 이전 값은 사라진다.


3. 여러 변수에 동시 할당
-------------------------------------------------
여러 변수에 같은 값을 한 번에 할당할 수 있다.
"""

a = b = c = 100

print(f'a = {a}')
print(f'b = {b}')
print(f'c = {c}')

"""
위 코드 결과:
a = 100
b = 100
c = 100

a = b = c = 100은 우측에서 좌측으로 진행되므로:
c에 100을 할당 → b에 c의 값을 할당 → a에 b의 값을 할당


4. 여러 변수에 다른 값 할당
-------------------------------------------------
여러 변수에 다른 값을 동시에 할당할 수 있다.
"""

