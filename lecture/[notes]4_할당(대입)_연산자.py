# 3년 후 총 금액
myMoney = myMoney + (myMoney * rate)

print(f'3년 후: {int(myMoney):,}원')

# 4년 후 총 금액
myMoney = myMoney + (myMoney * rate)

print(f'4년 후: {int(myMoney):,}원')

# 5년 후 총 금액
myMoney = myMoney + (myMoney * rate)

