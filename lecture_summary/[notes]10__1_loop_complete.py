"""
반복문(for문 & while문) 완전 정리
=================================================

1. 반복문이란?
-------------------------------------------------

반복문은:
"같은 코드를 여러 번 실행하기 위한 문법"

이다.

예시:
- 학생 30명 출력
- 게임 몬스터 반복 생성
- 1~100 출력
- 구구단 출력

같은 반복 작업을 자동화할 수 있다.


파이썬 대표 반복문:
1) for문   → 횟수 기반 반복
2) while문 → 조건 기반 반복
"""


"""
2. for문(for loop)
=================================================

for문은:
"정해진 횟수만큼 반복"

할 때 사용한다.

기본 형태:
"""

for num in range(1, 6):
    print(num)

"""
구조 설명:
-------------------------------------------------

for 변수 in iterable:
    실행문

- for      → 반복 시작 키워드
- 변수      → 반복될 때마다 값 저장
- in       → iterable 내부 데이터 접근
- iterable → 반복 가능한 객체
"""


"""
3. range() 함수
=================================================

range()는:
"연속된 숫자를 생성"

하는 함수이다.

형태:
range(시작값, 끝값, 단계)

주의:
끝값은 포함되지 않는다.
"""


for num in range(1, 6):
    print(num)

"""
실행 결과:
1
2
3
4
5

왜 5까지 출력될까?

range(1, 6)
→ 1 이상 6 미만
"""


"""
4. range() 간략화
=================================================
"""

"""
1) 단계 생략
-------------------------------------------------
단계(step)가 1이면 생략 가능
"""

for num in range(1, 6):
    print(num)

"""
2) 시작값 생략
-------------------------------------------------
시작값이 0이면 생략 가능
"""

for num in range(6):
    print(num)

"""
결과:
0
1
2
3
4
5
"""


"""
5. 짝수 출력하기
=================================================
"""

for num in range(2, 9, 2):
    print(num)

"""
결과:
2
4
6
8

의미:
2부터 시작해서
2씩 증가하면서
9 전까지 반복
"""


"""
6. 조건문과 반복문 함께 사용하기
=================================================
"""

for num in range(1, 11):

    if num % 3 == 0:
        print('3의 배수!')
    else:
        print(num)

"""
% 연산자:
나머지 연산자

num % 3 == 0
→ 3으로 나눴을 때 나머지가 0
→ 즉 3의 배수
"""


"""
7. 삼항 연산자 활용
=================================================
"""

for num in range(1, 11):
    print(f"{'3의 배수' if num % 3 == 0 else num}")

"""
구조:
-------------------------------------------------

참일 때 값 if 조건 else 거짓일 때 값
"""


"""
8. 사용자 입력 반복
=================================================
"""

count = int(input('횟수를 입력하세요: '))

for num in range(count):
    print(f'{num}: 메일 발송!')

"""
사용자가 입력한 횟수만큼 반복된다.
"""


"""
9. 구구단 출력하기
=================================================
"""

dan = int(input('원하는 구구단 입력: '))

for num in range(1, 10):
    print(f'{dan} X {num} = {dan * num}')

"""
구조 이해:
-------------------------------------------------

dan → 단수
num → 곱하는 숫자

2 x 1
2 x 2
2 x 3
...
"""


"""
10. 누적합 구하기
=================================================
"""

total = 0

for num in range(1, 11):
    total += num

print(f'total: {total}')

"""
동작 과정:
-------------------------------------------------

0 + 1
1 + 2
3 + 3
6 + 4
...

이런 방식으로 계속 누적된다.
"""


"""
11. 공배수와 최소공배수
=================================================
"""

minimum = 0

for num in range(1, 101):

    if num % 3 == 0 and num % 7 == 0:

        print(f'3과 7의 공배수: {num}')

        if minimum == 0:
            minimum = num

print(f'최소공배수: {minimum}')

"""
핵심 원리:
-------------------------------------------------

3과 7로 모두 나누어 떨어지면 공배수

minimum == 0:
→ 아직 최소공배수를 저장하지 않았다는 의미

가장 처음 발견된 공배수만 저장
"""


"""
12. 문자열 반복
=================================================
"""

for ch in 'hello':
    print(ch)

"""
문자열도 iterable 객체이다.

실행 결과:
h
e
l
l
o
"""


"""
13. while문(while loop)
=================================================

while문은:
"조건이 참(True)인 동안 반복"

한다.

기본 구조:
"""

num = 1

while num <= 5:

    print(num)

    num += 1

"""
구조 설명:
-------------------------------------------------

while 조건식:
    실행문

조건이 True면 계속 반복
False가 되면 종료
"""


"""
14. while문 핵심 개념
=================================================

while문에서 가장 중요한 것:
"조건을 언젠가는 False로 만들어야 한다"

안 그러면:
무한 루프 발생
"""

num = 1

while num < 5:

    print(num)

    num += 1

"""
num += 1 이 없으면:
1
1
1
1
...

무한 반복된다.
"""


"""
15. 홀수와 짝수 구분
=================================================
"""

num = 1

while num <= 10:

    if num % 2 == 0:
        print(f'{num}: 짝수')

    else:
        print(f'{num}: 홀수')

    num += 1


"""
16. while문 구구단
=================================================
"""

num = 1

while num <= 9:

    print(f'3 X {num} = {3 * num}')

    num += 1


"""
17. 중첩 반복문(Nested Loop)
=================================================

반복문 안에 반복문을 넣을 수 있다.
"""

dan = 2

while dan <= 9:

    print(f'--- {dan}단 ---')

    num = 1

    while num <= 9:

        print(f'{dan} X {num} = {dan * num}')

        num += 1

    dan += 1

"""
구조 이해:
-------------------------------------------------

바깥 반복문:
→ 단수 변경

안쪽 반복문:
→ 곱하기 숫자 반복
"""


"""
18. continue
=================================================

continue:
"현재 반복만 건너뛰고 다시 반복 시작"
"""

for num in range(1, 11):

    if num % 2 == 0:
        continue

    print(num)

"""
결과:
1
3
5
7
9

짝수는 건너뛴다.
"""


"""
19. break
=================================================

break:
"반복문 즉시 종료"
"""

for num in range(1, 11):

    if num == 5:
        break

    print(num)

"""
결과:
1
2
3
4

5를 만나자마자 종료
"""


"""
20. 누적합과 break
=================================================
"""

num = 1
total = 0

while num <= 10:

    total += num

    if total >= 30:
        print(f'현재 숫자: {num}')
        break

    num += 1

"""
동작 과정:
-------------------------------------------------

1 → 1
2 → 3
3 → 6
4 → 10
5 → 15
6 → 21
7 → 28
8 → 36

36이 30 이상이므로 종료
"""


"""
21. for ~ else
=================================================

for문이 정상 종료되면 else 실행
"""

for num in range(1, 6):
    print(num)

else:
    print('반복 완료')

"""
break 없이 끝까지 반복되면 else 실행
"""


"""
22. pass 키워드
=================================================

pass:
"아무것도 하지 않고 넘어감"
"""

for num in range(1, 6):
    pass

"""
주로:
- 임시 코드 작성
- 나중에 구현 예정

상황에서 사용
"""


"""
23. 삼각형 넓이 구하기
=================================================
"""

count = 1

while True:

    width = count * 2
    height = count * 3

    area = (width * height) / 2

    if area > 150:
        break

    print(f'삼각형 넓이: {area}')

    count += 1

"""
동작 원리:
-------------------------------------------------

가로:
2배씩 증가

세로:
3배씩 증가

넓이:
(가로 X 세로) / 2

넓이가 150 초과 시 종료
"""


"""
24. 핵심 정리
=================================================

for문:
→ 횟수 기반 반복

while문:
→ 조건 기반 반복

range():
→ 숫자 생성

continue:
→ 현재 반복 건너뛰기

break:
→ 반복 즉시 종료

중첩 반복문:
→ 반복문 안의 반복문
"""


