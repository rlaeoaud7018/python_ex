for name, score in students.items():
    print(f'{name}: {score}점')
    total += score

average = total / len(students)
print(f'\n평균: {average:.1f}점')

print('\n=== 평균 이상 ===')
for name, score in students.items():
    if score >= average:
        print(f'{name}: {score}점')

maxScore = 0
maxName = ''
for name, score in students.items():
    if score > maxScore:
        maxScore = score
        maxName = name

print(f'\n최고 점수: {maxName} ({maxScore}점)')


