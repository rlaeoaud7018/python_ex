"""
문제 10
-------------------------------------------------
random 모듈을 이용하여 숫자 맞추기 게임을 구현하시오.

조건:
- 1~100 사이의 난수를 생성한다.
- 사용자가 숫자를 입력하면 아래 결과를 출력한다.
  정답보다 크면  → '더 작은 숫자를 입력하세요.'
  정답보다 작으면 → '더 큰 숫자를 입력하세요.'
  정답이면       → '정답입니다! X번 만에 맞추셨습니다.'
- 최대 10번의 기회가 주어진다.
- 10번 안에 못 맞추면 '게임 오버! 정답은 XX 입니다.' 출력

출력 형식:
[1번째 시도] 숫자를 입력하세요: 50
더 큰 숫자를 입력하세요.
[2번째 시도] 숫자를 입력하세요: 75
더 작은 숫자를 입력하세요.
[3번째 시도] 숫자를 입력하세요: 63
정답입니다! 3번 만에 맞추셨습니다.
"""

# 여기에 코드를 작성하세요

import random

target = random.randint(1, 100)

def tryGame(attempt, guess):
    if guess > target:
        print('더 작은 숫자를 입력하세요.')
    elif guess < target:
        print('더 큰 숫자를 입력하세요.')
    else:
        print(f'정답입니다! {attempt}번 만에 맞추셨습니다.')

isCorrect = False

for attempt in range(1, 11):
    guess = int(input(f'[{attempt}번째 시도] 숫자를 입력하세요: '))

    if guess == target:
        tryGame(attempt, guess)
        isCorrect = True
        break
    else:
        tryGame(attempt, guess)

if isCorrect == False:
    print(f'게임 오버! 정답은 {target} 입니다.')



