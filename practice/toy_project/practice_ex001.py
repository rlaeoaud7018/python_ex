"""
문제 7
-------------------------------------------------
아래 학생 성적 데이터를 이용하여 조건에 맞게 출력하시오.

데이터:
scores = [88, 72, 95, 63, 80, 91, 55, 74, 89, 66]

조건:
- 전체 학생 수 출력
- 최고 점수 출력 (sort, min, max 사용 금지, 루프로 구현)
- 최저 점수 출력 (sort, min, max 사용 금지, 루프로 구현)
- 평균 점수 출력 (소수점 1자리)
- 80점 이상인 학생 수 출력

출력 형식:
전체 학생 수: 10명
최고 점수: 95점
최저 점수: 55점
평균 점수: 77.3점
80점 이상: 4명
"""

# 여기에 코드를 작성하세요
scores = [88, 72, 95, 63, 80, 91, 55, 74, 89, 66]

total = 0
max_score = scores[0]
min_score = scores[0]
count_above_80 = 0

for score in scores:
    total += score
    if score > max_score:
        max_score = score
    if score < min_score:
        min_score = score
    if score >= 80:
        count_above_80 += 1

average = total / len(scores)

print(f'전체 학생 수: {len(scores)}명')
print(f'최고 점수: {max_score}점')
print(f'최저 점수: {min_score}점')
print(f'평균 점수: {average:.1f}점')
print(f'80점 이상: {count_above_80}명')


